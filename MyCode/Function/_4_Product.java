public class _4_Product {

    public static int Product(int a , int b ) {
        return a * b ;
    }
    public static void main(String[] args) {
        int a = 10, b = 20 ;
        int product = Product(a, b); 
        System.out.println("Product of both number is = " + product);
    }
}
